// Driver SSD1306 128x64 por I2C para el display OLED del rover Olympus.
//
// Hardware: WeAct 0.96" OLED SSD1306, 128x64, I2C addr 0x3C, bus i2c-1
//   (GPIO2=SDA, GPIO3=SCL, pines 3 y 5 del header de 40 pines de la RPi5).
//
// No anade dependencias: usa unicamente rppal::i2c (ya vendoreado).
//
// Mapeo de memoria (page addressing mode del SSD1306):
//   - El GDDRAM son 128 columnas x 8 paginas (cada pagina = 8 filas).
//   - Cada byte del buffer es una columna dentro de una pagina; bit k
//     enciende el pixel de la fila (page*8 + k), bit 0 = arriba.

use rppal::i2c::I2c;

// Control bytes I2C del SSD1306.
const CTRL_CMD: u8 = 0x00; // Co=0, D/C=0 -> comando
const CTRL_DAT: u8 = 0x40; // Co=0, D/C=1 -> dato

pub struct Ssd1306 {
    i2c: I2c,
    width: u32,
    height: u32,
    pages: u32,
    buffer: Vec<u8>,
}

impl Ssd1306 {
    /// Abre el bus I2C por defecto (i2c-1 en RPi5) y configura el display.
    pub fn new(addr: u16, width: u32, height: u32) -> Result<Self, String> {
        let mut i2c = I2c::new().map_err(|e| format!("Error I2c::new: {}", e))?;
        i2c.set_slave_address(addr)
            .map_err(|e| format!("Error set_slave_address(0x{:02X}): {}", addr, e))?;

        let pages = height / 8;
        let mut dev = Ssd1306 {
            i2c,
            width,
            height,
            pages,
            buffer: vec![0u8; (width * pages) as usize],
        };
        dev.init()?;
        Ok(dev)
    }

    // --- Comandos de bajo nivel ---

    fn write_cmds(&mut self, cmds: &[u8]) -> Result<(), String> {
        let mut buf = Vec::with_capacity(cmds.len() + 1);
        buf.push(CTRL_CMD);
        buf.extend_from_slice(cmds);
        self.i2c
            .write(&buf)
            .map(|_| ())
            .map_err(|e| format!("Error I2C write(cmds): {}", e))
    }

    /// Secuencia de inicializacion SSD1306 para 128x64.
    fn init(&mut self) -> Result<(), String> {
        let mux = (self.height - 1) as u8;
        let com_pins = if self.height == 64 { 0x12 } else { 0x02 };
        self.write_cmds(&[
            0xAE, // display off
            0xD5, 0x80, // display clock divide
            0xA8, mux, // multiplex ratio (height-1)
            0xD3, 0x00, // display offset = 0
            0x40, // start line = 0
            0x8D, 0x14, // charge pump enable (0.96" necesita esto)
            0xA1, // segment remap: col 127 = col 0
            0xC8, // COM scan direction remap
            0xDA, com_pins, // COM pins hardware config
            0x81, 0xCF, // contrast
            0xD9, 0xF1, // pre-charge period
            0xDB, 0x40, // VCOMH deselect level
            0xA4, // resume display (contenido desde GDDRAM)
            0xA6, // normal (no invertido)
            0xAF, // display on
        ])?;
        self.clear_buffer();
        self.flush()?;
        Ok(())
    }

    /// Apaga el display (sin perder el buffer).
    pub fn power_off(&mut self) -> Result<(), String> {
        self.write_cmds(&[0xAE])
    }

    /// Enciende el display.
    pub fn power_on(&mut self) -> Result<(), String> {
        self.write_cmds(&[0xAF])
    }

    /// Ajusta el contraste (0-255).
    pub fn set_contrast(&mut self, value: u8) -> Result<(), String> {
        self.write_cmds(&[0x81, value])
    }

    /// Invierte/no-invierte los colores.
    pub fn invert(&mut self, on: bool) -> Result<(), String> {
        self.write_cmds(&[if on { 0xA7 } else { 0xA6 }])
    }

    // --- Framebuffer ---

    /// Borra solo el buffer en RAM (no toca el display hasta flush()).
    pub fn clear_buffer(&mut self) {
        self.buffer.fill(0);
    }

    /// Borra el buffer y lo envia al display.
    pub fn clear(&mut self) -> Result<(), String> {
        self.clear_buffer();
        self.flush()
    }

    /// Enciende/apaga un pixel. Coordenadas en pixels (x: 0..width, y: 0..height).
    pub fn set_pixel(&mut self, x: u32, y: u32, on: bool) {
        if x >= self.width || y >= self.height {
            return;
        }
        let page = (y / 8) as usize;
        let bit = (y % 8) as u8;
        let idx = page * self.width as usize + x as usize;
        if on {
            self.buffer[idx] |= 1 << bit;
        } else {
            self.buffer[idx] &= !(1 << bit);
        }
    }

    /// Envia el framebuffer completo al display, pagina a pagina.
    pub fn flush(&mut self) -> Result<(), String> {
        let w = self.width as usize;
        for p in 0..self.pages as usize {
            // Posicionar al inicio de la pagina p, columna 0.
            self.write_cmds(&[
                0xB0 | (p as u8), // set page start
                0x00, // lower column start = 0
                0x10, // higher column start = 0
            ])?;
            // Volcar los 128 bytes de la pagina.
            let mut buf = Vec::with_capacity(w + 1);
            buf.push(CTRL_DAT);
            buf.extend_from_slice(&self.buffer[p * w..(p + 1) * w]);
            self.i2c
                .write(&buf)
                .map(|_| ())
                .map_err(|e| format!("Error I2C write(page {}): {}", p, e))?;
        }
        Ok(())
    }

    /// Dibuja texto en la pagina `line` (0..pages), empezando en la columna `x`
    /// (en pixels, 0..width). La fuente es 8x8, asi que `line` seleciona una
    /// franja de 8 filas y cada caracter ocupa 8 columnas.
    /// Los caracteres fuera del rango ASCII imprimible (0x20-0x7E) se reemplazan
    /// por '?'. El texto que se salga del display se recorta.
    pub fn draw_text(&mut self, x: u32, line: u32, text: &str) {
        if line >= self.pages {
            return;
        }
        let w = self.width as usize;
        let page = line as usize;
        let mut col = x as usize;
        for ch in text.chars() {
            if col + 8 > w {
                break;
            }
            let code = ch as u32;
            let glyph = if (0x20..=0x7E).contains(&code) {
                &FONT_8X8[(code - 0x20) as usize]
            } else {
                &FONT_8X8[('?' as u32 - 0x20) as usize]
            };
            // Transponer de row-major (font) a column-major (GDDRAM):
            // cada byte del buffer es una columna de 8 pixeles verticales.
            for gc in 0..8 {
                let mut col_byte: u8 = 0;
                for row in 0..8 {
                    if (glyph[row] >> gc) & 1 != 0 {
                        col_byte |= 1 << row;
                    }
                }
                self.buffer[page * w + col + gc] = col_byte;
            }
            col += 8;
        }
    }
}

